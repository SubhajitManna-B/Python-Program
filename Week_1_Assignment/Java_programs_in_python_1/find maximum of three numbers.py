def find_maximum():
    num1 = float(input("Enter the first number: "))
    num2 = float(input("Enter the second number: "))
    num3 = float(input("Enter the third number: "))
    maximum = max(num1, num2, num3)
    print(f"The maximum of {num1}, {num2}, and {num3} is: {maximum}")

find_maximum()
